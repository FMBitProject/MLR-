export function formatDate(d: Date | number | null | undefined, locale: Locale): string {
  if (!d) return "—";
  return new Intl.DateTimeFormat(locale === "id" ? "id-ID" : "en-US", {
    day: "numeric",
    month: "short",
    year: "numeric",
  }).format(typeof d === "number" ? new Date(d) : d);
}

export function relativeDays(d: Date | number, locale: Locale): string {
  const ms = Date.now() - (typeof d === "number" ? d : d.getTime());
  const days = Math.floor(ms / 86_400_000);
  if (days <= 0) return locale === "id" ? "hari ini" : "today";
  return locale === "id" ? `${days} hari lalu` : `${days} days ago`;
}
